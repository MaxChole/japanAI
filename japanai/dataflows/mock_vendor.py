# ---------- 政策（户籍国 → 目标国购房政策）----------
def get_policy_faq(household_region: str, target_country: str = "日本") -> str:
    """
    返回「用户户籍/国籍所在国」在「目标国」买房的政策要点（mock）。
    不同国家居民在日本购房的限制、贷款、汇兑等政策不同。
    含购房资格、外汇与境外投资、贷款、申报与规划等，便于政策研究员产出完整报告。
    """
    return f"""# 购房政策与规划要点（Mock）- 户籍/国籍: {household_region} → 目标国: {target_country}

## 一、日本侧政策（目标国）
- 日本对外国人购房: 原则上无国籍限制，可购买房产（土地+建筑物）；贷款需日本境内银行或部分海外银行产品。
- 非居住者: 可购买、持有、出租、出售；出售时需源泉征收（税率 10.21%），租金收入源泉 20.42%。
- 日本无针对外国人的限购、限贷或额外审批；产权登记与日本人同等。

## 二、中国籍在日购房（户籍国政策与规划）
- 换汇与境外购房: 需遵守中国外汇管理规定，个人购汇用途如实申报；大额境外购房需合规换汇或境外已有资金。
- 境外投资登记: 根据《境外投资管理办法》等，境内主体境外投资需按规定办理备案/核准；个人境外购房通常需关注外汇额度与真实性审核。
- 日本侧: 对中国籍无额外限制，可正常签约、登记产权、委托管理。
- 税务规划: 非居住者需在日本申报/源泉；在中国需就境外所得按规定申报，避免双重征税可参考中日税收协定。

## 三、签证与长期规划
- 非居住者持有房产: 无需日本签证即可持有；若后续有居住或经营规划，需另行考虑签证类型（如经营管理、高度人才等）。
- 管理委托: 人在境外时建议委托日本管理公司负责招租、维修、报税等。
- 继承与赠与: 日本继承税/赠与税适用非居住者；若涉及跨境传承，需同时考虑中国与日本两国的遗产与税务规划。

## 四、数据来源与免责
- 以上为模拟摘要，具体以各国现行法规、外汇政策及律师/税务师意见为准；政策可能调整，请以最新口径为准。
"""


